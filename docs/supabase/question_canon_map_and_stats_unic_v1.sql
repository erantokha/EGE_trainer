-- Canonical map for prototype variants -> unique prototype (unic)
-- Generated from manifests (prototype_to_unic.json + prototype_to_type.json)
-- Purpose: compute statistics by unic_question_id while logging attempts by real prototype_id.

create table if not exists public.question_canon_map (
  question_id text primary key,
  unic_question_id text not null,
  type_id text not null,
  is_unic boolean not null default false
);

create index if not exists question_canon_map_unic_idx
  on public.question_canon_map (unic_question_id);

create index if not exists question_canon_map_type_idx
  on public.question_canon_map (type_id);

-- Recommended index for answer_events to speed up aggregates by student + question
create index if not exists answer_events_student_question_occurred_at_idx
  on public.answer_events (student_id, question_id, occurred_at desc);

-- RPC: stats for teacher, grouped by unic_question_id (unique prototypes)
create or replace function public.question_stats_for_teacher_unic_v1(
  p_student_id uuid,
  p_unic_question_ids text[]
)
returns table(
  unic_question_id text,
  total int,
  correct int,
  last_attempt_at timestamptz
)
language sql
stable
security definer
set search_path = public
as $$
  with allowed as (
    select 1
    from public.teacher_students ts
    where ts.teacher_id = auth.uid()
      and ts.student_id = p_student_id
    limit 1
  )
  select
    qcm.unic_question_id,
    count(*)::int as total,
    count(*) filter (where ae.correct)::int as correct,
    max(ae.occurred_at) as last_attempt_at
  from public.answer_events ae
  join public.question_canon_map qcm
    on qcm.question_id = ae.question_id
  where exists (select 1 from allowed)
    and ae.student_id = p_student_id
    and qcm.unic_question_id = any(p_unic_question_ids)
  group by qcm.unic_question_id
  order by qcm.unic_question_id;
$$;

revoke execute on function public.question_stats_for_teacher_unic_v1(uuid, text[]) from anon;
grant execute on function public.question_stats_for_teacher_unic_v1(uuid, text[]) to authenticated;

